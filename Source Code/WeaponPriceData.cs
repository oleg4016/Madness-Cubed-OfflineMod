        public static void Populate()
        {
            if (Kube.GPS == null) return;
            try
            {
                PopulateWeaponPrices();
                PopulateUpgradePrices();
                Debug.Log("[Offline] Weapon prices populated from dump data");
            }
            catch (Exception ex)
            {
                Debug.LogError("[Offline] Error populating weapon prices: " + ex.Message);
            }
        }

        private static void PopulateWeaponPrices()
        {
            int wCount = Math.Min(WeaponBuyPrices.GetLength(0), Kube.GPS.weaponsPrice1.GetLength(0));
            int tCount = Math.Min(3, Kube.GPS.weaponsPrice1.GetLength(1));
            for (int w = 0; w < wCount; w++)
            {
                Kube.GPS.weaponsPrice1[w, 0] = WeaponBuyPrices[w, 0];
                Kube.GPS.weaponsPrice2[w, 0] = WeaponBuyPrices[w, 1];
                for (int t = 1; t < tCount; t++)
                {
                    Kube.GPS.weaponsPrice1[w, t] = (int)(WeaponBuyPrices[w, 0] * (t + 1) * 0.8);
                    Kube.GPS.weaponsPrice2[w, t] = (int)((WeaponBuyPrices[w, 1] > 0 ? WeaponBuyPrices[w, 1] : 5) * (t + 1));
                }
            }
        }

        private static void PopulateUpgradePrices()
        {
            var gps = Kube.GPS;
            int maxWeapons = Math.Min(WeaponUpgradePrices.Length, gps.upgradePrice.GetLength(0));
            for (int w = 0; w < maxWeapons; w++)
            {
                var priceArr = WeaponUpgradePrices[w];
                if (priceArr == null || priceArr.Length == 0) continue;
                for (int q = 0; q < 4; q++)
                {
                    for (int lvl = 0; lvl < priceArr.Length; lvl++)
                    {
                        int val = priceArr[lvl];
                        if (val > 0)
                            gps.upgradePrice[w, q, lvl] = new PriceValue(val, false);
                        else if (val < 0)
                            gps.upgradePrice[w, q, lvl] = new PriceValue(-val, true);
                    }
                }
            }
        }
